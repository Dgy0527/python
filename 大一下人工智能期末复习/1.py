'''
1、题目简述
基尼系数是指国际上通用的、用以衡量一个国家或地区居民收入差距的常用指标。
基尼系数最大为1,最小等于0。
基尼系数越接近0表明收入分配越是趋向平等。
国际惯例把指数在0.2以下视为收入绝对平均,指数在0.2至0.3视为收入比较平均；
指数在0.3至0.4视为收入相对合理；
指数在0.4至0.5视为收入差距较大,当基尼系数达到0.5以上时，则表示收入悬殊。

基尼系数	收入差距
0-0.2	   绝对平均
0.2-0.3	   比较平均
0.3-0.4	   相对合理
0.4-0.5	   差距较大
0.5以上	   收入悬殊

程序说明
1.输入基尼系数
2.输出该基尼系数对应收入差距
样例输入
请输入基尼系数:0.35
样例输出
基尼系数为:0.35属于：相对合理

样例输入
请输入基尼系数:0.35
样例输出
基尼系数为:0.35属于：相对合理

'''

#参考答案
income=eval(input('请输入基尼系数:'))
belong=''

if income<=0.2:
    belong='绝对平均'
elif income<0.3:
    belong='比较平均'
elif income<=0.4:
    belong='相对合理'
elif income<0.5:
    belong='差距较大'
else:
    belong='收入悬殊'

print('基尼系数为:'+str(income)+'属于:'+belong) 

